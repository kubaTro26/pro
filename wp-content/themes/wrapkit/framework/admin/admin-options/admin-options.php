	function wrapkit_compiler_action( $options, $css ) {
		//echo '<h1>The compiler hook has run!';
		//print_r($options); //Option values
		//print_r($css); // compiler selector CSS values  compiler => array( CSS SELECTORS )
	
		
	  // Custom static CSS file
		if ( is_multisite() ) {
			$filename = get_template_directory() . '/framework/admin/admin-options/custom-styles-' . get_current_blog_id() . '.css';
		} else {
			$filename = get_template_directory() . '/framework/admin/admin-options/custom-styles' . '.css';
		}
		global $wp_filesystem;

		if ( empty( $wp_filesystem ) ) {
			require_once( ABSPATH .'/wp-admin/includes/file.php' );
			WP_Filesystem();
		}
	
		if ( $wp_filesystem ) {
				$wp_filesystem->put_contents(
				$filename,
				$css,
				FS_CHMOD_FILE // predefined mode settings for WP files
			);
		}
	}
		
	add_filter( 'redux/options/wrapkit_data/compiler', 'wrapkit_compiler_action', 10, 2 );
	
		
